N EQU 50H
org 00h
ljmp main

DELAY_OF_50_MS:
	MOV R4,#200
	BACK1:
	MOV R3,#0FFH
	BACK :
	DJNZ R3, BACK
	DJNZ R4, BACK1
RET

DELAY_OF_1_S:
	MOV R2, #1BH
	LOOP500 :
		ACALL DELAY_OF_50_MS
		DJNZ R2, LOOP500
RET

DISPLAY:
	USING 0
	MOV A,N
	MOV R0,A
	MOV A,51H
	MOV R1,A
	LOOP:
		MOV A,@R1
		ANL A,#0XF0
		MOV P1,A
		ACALL DELAY_OF_1_S
		INC R1
		DJNZ R0,LOOP
RET

ORG 100H
MAIN:
	ACALL DISPLAY
	HERE: SJMP HERE
END